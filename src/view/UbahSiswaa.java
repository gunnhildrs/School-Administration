    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {
        // Validasi field wajib
        if (
            jTextField2.getText().trim().isEmpty() ||  // nama
            jTextField4.getText().trim().isEmpty() ||  // NIS
            jTextField5.getText().trim().isEmpty() ||  // NISN
            jTextField6.getText().trim().isEmpty() ||  // tempat lahir
            jTextField8.getText().trim().isEmpty() ||  // no telp
            jTextField3.getText().trim().isEmpty() ||  // tahun masuk (gunakan jTextField3 bukan jTextField1)
            jTextArea1.getText().trim().isEmpty() ||   // alamat
            jComboBox4.getSelectedItem() == null ||    // JK
            jComboBox5.getSelectedItem() == null ||    // tanggal
            jComboBox9.getSelectedItem() == null ||    // bulan
            jComboBox1.getSelectedItem() == null ||    // tahun
            jComboBox8.getSelectedItem() == null       // agama
        ) {
            JOptionPane.showMessageDialog(this, "Harap isi semua field yang diperlukan!");
            return;
        }

        // Format tanggal lahir: Tahun-Bulan-Tanggal (YYYY-MM-DD)
        String tanggalLahir = jComboBox1.getSelectedItem() + "-" +
                String.format("%02d", jComboBox9.getSelectedIndex() + 1) + "-" +
                String.format("%02d", Integer.parseInt((String)jComboBox5.getSelectedItem()));

        // Koneksi ke database
        Connection conn = getConnection();
        if (conn != null) {
            String query = "UPDATE siswa SET nama=?, JK=?, NISN=?, tempatLahir=?, " +
                    "tanggalLahir=?, agama=?, alamat=?, noTelp=?, tahunMasuk=?, status=? " +
                    "WHERE NIS=?";

            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, jTextField2.getText()); // nama
            pst.setString(2, (String) jComboBox4.getSelectedItem()); // JK
            pst.setString(3, jTextField5.getText()); // NISN
            pst.setString(4, jTextField6.getText()); // tempat lahir
            pst.setString(5, tanggalLahir); // tanggal lahir
            pst.setString(6, (String) jComboBox8.getSelectedItem()); // agama
            pst.setString(7, jTextArea1.getText()); // alamat
            pst.setString(8, jTextField8.getText()); // no telp
            pst.setString(9, jTextField3.getText()); // tahun masuk
            pst.setString(10, jTextField1.getText()); // status
            pst.setString(11, jTextField4.getText()); // NIS (WHERE condition)

            int result = pst.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data siswa berhasil diperbarui!");
                // Kembali ke DataSiswaa
                DataSiswaa dataSiswaForm = new DataSiswaa();
                dataSiswaForm.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal memperbarui data siswa!");
            }

            pst.close();
            conn.close();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saat menyimpan data: " + e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Format tanggal tidak valid: " + e.getMessage());
    }
        
    DataSiswaa dataSiswaForm = new DataSiswaa();
        dataSiswaForm.setVisible(true);
        this.dispose();    
        
    }//GEN-LAST:event_jButton6ActionPerformed

