private Connection getConnection() {
    try {
        String url = "jdbc:mysql://localhost:3306/tiara_school";
        String username = "root"; // sesuaikan dengan username MySQL Anda
        String password = "";     // sesuaikan dengan password MySQL Anda
        return DriverManager.getConnection(url, username, password);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Koneksi database gagal: " + e.getMessage());
        return null;
    }
}

private void fillFormWithData(String[] studentData) {
    if (studentData == null || studentData.length < 11) return;
    
    try {
        // Fill the form fields
        jTextField2.setText(studentData[0]); // nama
        jTextField4.setText(studentData[1]); // NIS
        jComboBox4.setSelectedItem(studentData[2]); // JK
        jTextField5.setText(studentData[3]); // NISN
        jTextField6.setText(studentData[4]); // tempat lahir
        
        // Parse and set tanggal lahir
        if (studentData[5] != null && !studentData[5].isEmpty()) {
            String[] dateParts = studentData[5].split("-");
            if (dateParts.length == 3) {
                jComboBox5.setSelectedItem(dateParts[2]); // tanggal
                jComboBox9.setSelectedIndex(Integer.parseInt(dateParts[1]) - 1); // bulan
                jComboBox1.setSelectedItem(dateParts[0]); // tahun
            }
        }
        
        jComboBox8.setSelectedItem(studentData[6]); // agama
        jTextArea1.setText(studentData[7]); // alamat
        jTextField8.setText(studentData[8]); // no telp
        jTextField3.setText(studentData[9]); // tahun masuk (gunakan jTextField3)
        
        // Status (if available)
        if (studentData.length > 10) {
            jTextField1.setText(studentData[10]); // status
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error filling form: " + e.getMessage());
    }
}

